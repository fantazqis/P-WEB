    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $post = Review::find($id);
        
        return view('admin.updatereview')->with('post',$post);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $this->validate($request,[
            'title' => 'required',
            'name' => 'required',
            'detail' => 'required'
        ]);

        //create post
        $wisata = Review::find($id);
        $wisata->title = $request->input('title');
        $wisata->name = $request->input('name');
        $wisata->detail = $request->input('detail');

        $wisata->save();
        
        return redirect('/adminreviews')->with('success','Post Updated');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $post = Review::find($id);

        $post->delete();
        return redirect('/adminreviews')->with('success','Post Removed');
    }
